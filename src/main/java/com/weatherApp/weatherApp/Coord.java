package com.weatherApp.weatherApp;


public class Coord {
    private String lon;
    private String lat;

    public Coord() {
    }



    public String getLon() {
        return lon;
    }

    public void setLon(String lon) {
        this.lon = lon;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

}
